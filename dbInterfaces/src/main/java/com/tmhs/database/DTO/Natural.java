package com.tmhs.database.DTO;

/**
 * @author TMHYXZ6
 * 
 */
public class Natural {
	private String id;
	private String plateNum;
	private String wellNum;
	private String gpCode;
	private String molWeight;
	private String name;
	private String CASNo;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the plateNum
	 */
	public String getPlateNum() {
		return plateNum;
	}

	/**
	 * @param plateNum
	 *            the plateNum to set
	 */
	public void setPlateNum(String plateNum) {
		this.plateNum = plateNum;
	}

	/**
	 * @return the wellNum
	 */
	public String getWellNum() {
		return wellNum;
	}

	/**
	 * @param wellNum
	 *            the wellNum to set
	 */
	public void setWellNum(String wellNum) {
		this.wellNum = wellNum;
	}

	/**
	 * @return the gpCode
	 */
	public String getGpCode() {
		return gpCode;
	}

	/**
	 * @param gpCode
	 *            the gpCode to set
	 */
	public void setGpCode(String gpCode) {
		this.gpCode = gpCode;
	}

	/**
	 * @return the molWeight
	 */
	public String getMolWeight() {
		return molWeight;
	}

	/**
	 * @param molWeight
	 *            the molWeight to set
	 */
	public void setMolWeight(String molWeight) {
		this.molWeight = molWeight;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the cASNo
	 */
	public String getCASNo() {
		return CASNo;
	}

	/**
	 * @param cASNo
	 *            the cASNo to set
	 */
	public void setCASNo(String cASNo) {
		CASNo = cASNo;
	}
}
